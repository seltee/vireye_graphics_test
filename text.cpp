void Text::displayString(Engine *engine, char *string, uint8_t color, uint16_t x, uint16_t y){
	uint8_t len = strlen(string);
	for (uint8_t i = 0; i < len; i++){
		char c = string[i];
		if (c >= 'a' && c <= 'z'){
			c -= 0x20;
		}
		
		if (c >= 'A' && c <= 'Z'){
			engine->displaySpriteMask(symbols[c-'A'], color, x, y, 1, 12);
		}
		
		if (c >= '0' && c <= '9'){
			engine->displaySpriteMask(symbols[c-'0'+26], color, x, y, 1, 12);
		}
		
		if (c == '-'){
			engine->displaySpriteMask(minus, color, x, y, 1, 12);
		}
		
		if (c == '+'){
			engine->displaySpriteMask(plus, color, x, y, 1, 12);
		}
		
		if (c == '\''){
			engine->displaySpriteMask(quote, color, x, y, 1, 12);
		}
		
		x+=16;
	}
}





